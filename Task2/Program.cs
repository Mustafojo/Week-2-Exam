using math;

var math = new math.Math();
System.Console.Write("Abs: ");
System.Console.WriteLine(math.Abs(-10));

System.Console.Write("Pow: ");
System.Console.WriteLine(math.Pow(10,2));

System.Console.Write("Sqrt: ");
System.Console.WriteLine(math.Sqrt(3));

System.Console.Write("Max: ");
System.Console.WriteLine(math.Max(10,20));

System.Console.Write("Min: ");
System.Console.WriteLine(math.Min(10,20));

